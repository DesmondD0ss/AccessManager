/**
 * Initialise le syst√®me de permissions
 */
export async function initializePermissions() {
  try {
    logger.info('üöÄ Initialisation du syst√®me de permissions...');

    // V√©rifier s'il existe d√©j√† un utilisateur syst√®me pour cr√©er les r√¥les
    let systemUser = await prisma.user.findFirst({
      where: { 
        OR: [
          { role: 'SUPER_ADMIN' },
          { username: 'system' }
        ]
      }
    });

    // Si aucun utilisateur syst√®me n'existe, cr√©er un utilisateur syst√®me temporaire
    if (!systemUser) {
      systemUser = await prisma.user.create({
        data: {
          username: 'system',
          email: 'system@localhost',
          role: 'SUPER_ADMIN',
          isActive: true,
          hashedPassword: 'system' // Sera d√©sactiv√© plus tard
        }
      });
      logger.info('üë§ Utilisateur syst√®me cr√©√©');
    }

    // 1. Cr√©er les permissions
    logger.info('üìù Cr√©ation des permissions...');
    for (const permission of DEFAULT_PERMISSIONS) {
      await prisma.permission.upsert({
        where: { name: permission.name },
        update: {
          description: permission.description,
          resource: permission.resource
        },
        create: permission
      });
    }
    logger.info(`‚úÖ ${DEFAULT_PERMISSIONS.length} permissions cr√©√©es/mises √† jour`);

    // 2. Cr√©er les r√¥les
    logger.info('üë• Cr√©ation des r√¥les...');
    for (const roleData of DEFAULT_ROLES) {
      const { permissions, ...roleInfo } = roleData;
      
      const role = await prisma.customRole.upsert({
        where: { name: roleData.name },
        update: {
          displayName: roleInfo.displayName,
          description: roleInfo.description,
          isSystem: roleInfo.isSystem
        },
        create: roleInfo
      });

      // 3. Associer les permissions au r√¥le
      logger.info(`üîó Association des permissions au r√¥le ${role.displayName}...`);
      
      // Supprimer les associations existantes
      await prisma.rolePermission.deleteMany({
        where: { roleId: role.id }
      });

      // Cr√©er les nouvelles associations
      for (const permissionName of permissions) {
        const permission = await prisma.permission.findUnique({
          where: { name: permissionName }
        });

        if (permission) {
          await prisma.rolePermission.create({
            data: {
              roleId: role.id,
              permissionId: permission.id
            }
          });
        }
      }
    }
    logger.info(`‚úÖ ${DEFAULT_ROLES.length} r√¥les cr√©√©s/mis √† jour avec leurs permissions`);

    // 4. Migrer les utilisateurs existants vers le nouveau syst√®me
    logger.info('üîÑ Migration des utilisateurs vers le nouveau syst√®me de r√¥les...');
    const users = await prisma.user.findMany({
      include: { userRoles: true }
    });

    for (const user of users) {
      // Si l'utilisateur n'a pas encore de r√¥les assign√©s
      if (user.userRoles.length === 0) {
        let roleName = 'user'; // Par d√©faut

        // Mapper l'ancien syst√®me de r√¥les
        switch (user.role) {
          case 'SUPER_ADMIN':
            roleName = 'super_admin';
            break;
          case 'ADMIN':
            roleName = 'admin';
            break;
          case 'MODERATOR':
            roleName = 'moderator';
            break;
          default:
            roleName = 'user';
        }

        const role = await prisma.customRole.findUnique({
          where: { name: roleName }
        });

        if (role) {
          await prisma.userRole.create({
            data: {
              userId: user.id,
              roleId: role.id,
              assignedAt: new Date()
            }
          });
          logger.info(`üë§ Utilisateur ${user.username || user.email} assign√© au r√¥le ${role.displayName}`);
        }
      }
    }

    logger.info('üéâ Syst√®me de permissions initialis√© avec succ√®s !');
    
    // Afficher un r√©sum√©
    const permissionCount = await prisma.permission.count();
    const roleCount = await prisma.role.count();
    const userRoleCount = await prisma.userRole.count();
    
    logger.info('üìä R√©sum√© :');
    logger.info(`   ‚Ä¢ ${permissionCount} permissions disponibles`);
    logger.info(`   ‚Ä¢ ${roleCount} r√¥les configur√©s`);
    logger.info(`   ‚Ä¢ ${userRoleCount} attributions de r√¥les`);

  } catch (error) {
    logger.error('‚ùå Erreur lors de l\'initialisation des permissions:', error);
    throw error;
  }
}

